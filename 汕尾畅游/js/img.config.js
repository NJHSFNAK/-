const imageLayers = [];
// 加载静态图片
function setImageLayer() {
  config.map((item) => {
    var imageLayer = new AMap.ImageLayer({
      url: getImgSrc(item.imageUrl),
      opacity: 1,
      bounds: new AMap.Bounds(...item.bounds),
      zooms: [1, 20],
    });
    imageLayers.push(imageLayer);
  });
}

